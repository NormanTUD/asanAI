	function checkSubElementsKeys($array) {
		$keys = [];

		foreach ($array as $lang => $subArray) {
			if (!is_array($subArray)) {
				die("Sub-element is not an array");
			}

			$subKeys = array_keys($subArray);

			if (empty($keys)) {
				$keys = $subKeys;
			} elseif ($keys !== $subKeys) {
				$missingKeys = array_diff($keys, $subKeys);
				if ($missingKeys) {
					die("Missing key in '$lang': " . reset($missingKeys) . "\n");
				}

				$extraKeys = array_diff($subKeys, $keys);
				if ($extraKeys) {
					die("Extra key in '$lang' not found in first language: " . reset($extraKeys) . "\n");
				}
			}
		}

		return true;
	}

